# criação da variável com um valor inicial
i = 0
# enquanto a variável contadora não chegar ao limite
while i < 10:
    # para cada uma das repetições uma mensagem é exibida
    print(f"Mais uma mensagem, com i valendo: {i + 1}")
    i = i + 1

